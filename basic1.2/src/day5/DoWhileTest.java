package day5;

public class DoWhileTest {

	public static void main(String[] args) {
			
		
		//while 문
		boolean isTrue = false;
		while(isTrue) {
			System.out.println("while 안녕하세요");
		}
		
		
		//do while문
		do {
			System.out.println("do-while");
		} while(isTrue);
		
		
		
	}

}
