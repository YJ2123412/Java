package day5;

public class assa {

	public static void main(String[] args) {

		
		
		// & (논리곱)
		System.out.println(10&11);
		// | (논리합)
		System.out.println(10|11);
		//^(베타 논리합): 서로 다르면 1,같으면 0
		System.out.println(10^11);
		// ~ 논리의 부정
		System.out.println(~10);
		
		
		//쉬프트 연산자
		System.out.println(10 >> 1);
		System.out.println(10 << 1);
	}

}
